            SELECT CONCAT('SE HA AÑADIDO EL TRABAJADOR. NÚMERO ACTUAL DE TRABAJADORES: ', TOTAL_TRABAJADORES, ' (PLANTILLA REDUCIDA)') AS MENSAJE;
        END IF;
    END IF;
END;

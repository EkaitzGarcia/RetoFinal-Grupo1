Create procedure COMPRA_COMPLETA (IN id_compra INT, IN fecha_compra DATE, IN metodo ENUM('EFECTIVO', 'TARJETA'), IN dni_cliente CHAR(9), IN nss_trabajador CHAR(12))
BEGIN
    DECLARE duplicado BOOL DEFAULT 0;
    DECLARE CONTINUE HANDLER FOR 1062 SET duplicado = 1;

    IF NOT EXISTS (SELECT 1 FROM CLIENTE WHERE DNI = dni_cliente) THEN
        SELECT 'Error: el cliente no existe' AS MENSAJE;

    ELSEIF NOT EXISTS (SELECT 1 FROM TRABAJADOR WHERE NSS = nss_trabajador) THEN
        SELECT 'Error: el trabajador no existe' AS MENSAJE;

    ELSE
        INSERT INTO COMPRA (ID, FECHA, TOTAL, METODO_PAGO, DNI, NSS)
        VALUES (id_compra, fecha_compra, 0, metodo, dni_cliente, nss_trabajador);

        IF duplicado = 1 THEN
            SELECT 'Error: ya existe una compra con ese ID' AS MENSAJE;
        ELSE
            SELECT 'Compra creada correctamente' AS MENSAJE;
        END IF;
    END IF;
END //


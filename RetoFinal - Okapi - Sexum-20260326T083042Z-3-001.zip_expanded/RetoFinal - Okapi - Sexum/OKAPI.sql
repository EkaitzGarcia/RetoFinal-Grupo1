USE OKAPI;

CREATE TABLE TRABAJADOR(
NSS CHAR(12) PRIMARY KEY,
NOMBRE_T VARCHAR(50),
APELLIDO_T VARCHAR(50),
TELEFONO_T CHAR(9),
CORREO_T VARCHAR(100)
);

CREATE TABLE CLIENTE(
DNI CHAR(9) PRIMARY KEY,
NOMBRE_C VARCHAR(50),
APELLIDO_C VARCHAR(50),
TELEFONO_C CHAR(9),
CORREO_C VARCHAR(100),
DIRECCION VARCHAR(100)
);

CREATE TABLE PRODUCTO(
NOMBRE VARCHAR(40),
REF VARCHAR(10) PRIMARY KEY,
PRECIO DECIMAL(10,2) CHECK (PRECIO > 0),
DESCUENTO INT DEFAULT 0 CHECK (DESCUENTO >=0 AND DESCUENTO <100)
);

CREATE TABLE COMPRA(
ID INT PRIMARY KEY,
FECHA DATE,
TOTAL DECIMAL(10,2) CHECK (TOTAL >=0),
METODO_PAGO ENUM('EFECTIVO','TARJETA'),
DNI CHAR(9),
NSS CHAR(12),
FOREIGN KEY (DNI) REFERENCES CLIENTE(DNI),
FOREIGN KEY (NSS) REFERENCES TRABAJADOR(NSS)
);

CREATE TABLE ESTA(
ID INT,
REF VARCHAR(10),
CANTIDAD INT CHECK (CANTIDAD >0),
PRIMARY KEY(ID,REF),
FOREIGN KEY(ID) REFERENCES COMPRA(ID),
FOREIGN KEY(REF) REFERENCES PRODUCTO(REF)
);


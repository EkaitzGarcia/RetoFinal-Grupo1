CREATE PROCEDURE ALTATRABAJADOR_VALIDADO (
    IN P_NSS CHAR(12),
    IN P_NOMBRE VARCHAR(50),
    IN P_APELLIDO VARCHAR(50),
    IN P_TELEFONO CHAR(9),
    IN P_CORREO VARCHAR(100)
)
BEGIN
    DECLARE EXISTE INT;
    DECLARE TOTAL_TRABAJADORES INT;

    -- COMPROBAR SI EL TRABAJADOR YA EXISTE
    SELECT COUNT(*) INTO EXISTE
    FROM TRABAJADOR
    WHERE NSS = P_NSS;

    IF EXISTE > 0 THEN
        SELECT CONCAT('EL TRABAJADOR CON NSS ', P_NSS, ' YA EXISTE') AS MENSAJE;
    ELSE
        -- INSERTAR EL TRABAJADOR
        INSERT INTO TRABAJADOR ()
        VALUES (P_NSS, P_NOMBRE, P_APELLIDO, P_TELEFONO, P_CORREO);

        -- CONTAR TOTAL DE TRABAJADORES
        SELECT COUNT(*) INTO TOTAL_TRABAJADORES
        FROM TRABAJADOR;

        -- MENSAJE SEGÚN PLANTILLA AMPLIA O REDUCIDA
        IF TOTAL_TRABAJADORES > 5 THEN
            SELECT CONCAT('SE HA AÑADIDO EL TRABAJADOR. NÚMERO ACTUAL DE TRABAJADORES: ', TOTAL_TRABAJADORES, ' (PLANTILLA AMPLIA)') AS MENSAJE;

CREATE FUNCTION AhorroCliente(P_DNI VARCHAR(40))
RETURNS DECIMAL(10,2)
DETERMINISTIC
BEGIN
    DECLARE V_A DECIMAL(10,2);
    DECLARE V_E INT;

    SELECT COUNT(*) INTO V_E FROM CLIENTE WHERE DNI = P_DNI;

    IF V_E = 0 THEN
        RETURN -1;
    END IF;

    SELECT SUM(PR.PRECIO * (PR.DESCUENTO / 100.0) * E.CANTIDAD) INTO V_A FROM COMPRA C
    JOIN ESTA E ON E.ID = C.ID JOIN PRODUCTO PR ON PR.REF = E.REF
    WHERE C.DNI = P_DNI;

    IF V_A IS NULL THEN SET V_A = 0;
    END IF;

    RETURN V_A;
END //
DELIMITER ;


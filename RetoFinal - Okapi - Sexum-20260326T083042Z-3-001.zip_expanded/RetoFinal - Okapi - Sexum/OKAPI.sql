CREATE FUNCTION TOTALFINALCOMPRAAVANZADO(P_ID INT)
RETURNS DECIMAL(10,2)
DETERMINISTIC
BEGIN
    DECLARE V_TOTAL DECIMAL(10,2);
    DECLARE V_EXISTE INT;

    -- COMPROBAR SI LA COMPRA EXISTE
    SELECT COUNT(*) INTO V_EXISTE 
    FROM COMPRA 
    WHERE ID = P_ID;

    IF V_EXISTE = 0 THEN
        RETURN -1;
    END IF;

    -- CALCULAR TOTAL CON DESCUENTOS DE PRODUCTOS
    SELECT SUM(E.CANTIDAD * P.PRECIO * (1 - P.DESCUENTO/100))
    INTO V_TOTAL
    FROM ESTA E
    JOIN PRODUCTO P ON E.REF = P.REF
    WHERE E.ID = P_ID;

    SET V_TOTAL = IFNULL(V_TOTAL,0);

    -- APLICAR DESCUENTO ADICIONAL SI SUPERA 100€
    IF V_TOTAL > 100 THEN
        SET V_TOTAL = V_TOTAL * 0.95;
    END IF;

    RETURN V_TOTAL;

END;
